class AAColor:
    black: str = "black"

    darkGray: str = "darkGray"

    lightGray: str = "lightGray"

    white: str = "white"

    gray: str = "gray"

    red: str = "red"

    green: str = "green"

    blue: str = "blue"

    cyan: str = "cyan"

    yellow: str = "yellow"

    magenta: str = "magenta"

    orange: str = "orange"

    purple: str = "purple"

    brown: str = "brown"

    clear: str = "clear"
  
    def rgbaColor(red: int, green: int, blue: int, alpha: float):
        return f"rgba({red},{green},{blue},{alpha}"
  
  
    